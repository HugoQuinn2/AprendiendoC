#include <iostream>
using namespace std;

int main(){
	float n1;
	float n2;
	
	cout<<"Digita el primer numero: ";
	cin>> n1;
	cout<<"Digita el 2do numero: ";
	cin>> n2;
	
	cout<< n1 << "+"<< n2 << " = " <<n1+n2<<endl;
	cout<< n1 << "-"<< n2 << " = " <<n1-n2<<endl;
	cout<< n1 << "x"<< n2 << " = " <<n1*n2<<endl;
	cout<< n1 << "/"<< n2 << " = " <<n1/n2<<endl;
	return 0;
}
